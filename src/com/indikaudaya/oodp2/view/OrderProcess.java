    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTextField1.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter order number", "Order Section", JOptionPane.ERROR_MESSAGE);
        } else {
            OrderCheckController orderCheckController = new OrderCheckController();
            Orders searchOrder = orderCheckController.searchOrder(Integer.parseInt(jTextField1.getText()));
            if (searchOrder == null) {
                jLabel4.setText("Please check order number");
            } else {
                OrderEnum currentStep = searchOrder.getCurrentStep();
                jLabel4.setText(currentStep.toString());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

