    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        UpdateOrderStatusController upOrder = new UpdateOrderStatusController();
        if (jComboBox1.getSelectedItem().toString().equals(OrderEnum.COOKING.toString())) {           
            upOrder.updateOrderStatusAsCooking(Integer.parseInt(jTextField1.getText()));          
        } else if (jComboBox1.getSelectedItem().toString().equals(OrderEnum.PACKING.toString())) {
            upOrder.updateOrderStatusAsPacking(Integer.parseInt(jTextField1.getText()));           
        } else if (jComboBox1.getSelectedItem().toString().equals(OrderEnum.HANDOVER.toString())) {
            upOrder.updateOrderStatusAsHandOvered(Integer.parseInt(jTextField1.getText()));            
        }
    }//GEN-LAST:event_jButton3ActionPerformed

