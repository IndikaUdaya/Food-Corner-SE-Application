    private void loadDefaultValus() {
        OrderEnum[] loadOrderStatus = new LoadDefaultDataController(null).loadOrderStatus();
        DefaultComboBoxModel<String> model = (DefaultComboBoxModel<String>) jComboBox1.getModel();
        model.removeAllElements();
        model.addElement("Select Order Status");
        Vector v = new Vector();
        for (Object object : loadOrderStatus) {
            v.add(object.toString());
        }
        model.addAll(v);
    }

    private void clearAll() {
        jLabel4.setText(null);
        jTextField1.setText(null);
        loadDefaultValus();
    }
